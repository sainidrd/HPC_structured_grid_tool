function [f] = nonlinear(n,r1,rn,s1,sn)
format long;
%n=100;
%r1=0.5;
%rn=1.0;
%s1=0.001;
%sn=0.01;
b = zeros(4,1);
x = zeros(4,1);
%f = zeros(n,1);
%lc = ra*(rn-r1);
b(1)=r1;
b(2)=rn;
b(3)=s1;
b(4)=sn;

a=zeros(4,4);
a(1,1)=1.0;
a(1,2)=1.0;
a(1,3)=1.0;
a(1,4)=1.0;

a(2,1)=1.0;
a(2,2)=1.0*n;
a(2,3)=1.0*(n^3);
a(2,4)=1.0*(n^5);

a(3,1)=0.0;
a(3,2)=1.0;
a(3,3)=1.0*3;
a(3,4)=1.0*5;

a(4,1)=0.0;
a(4,2)=1.0;
a(4,3)=3.0*(n^2);
a(4,4)=5.0*(n^4);

x = inv(a)*b;

for i=1:n
   f(i)=x(1)+x(2)*i+x(3)*(i^3)+x(4)*(i^5); 
end
end
